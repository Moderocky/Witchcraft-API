/**
 * All the individual outfit slots.
 * A user can wear an individual item of clothing in each slot.
 * A user cannot wear two items of clothing in the same slot.
 */
public enum Clothing {
    JACKET,
    SHIRT,
    LEGS,
    LEFT_SLEEVE,
    RIGHT_SLEEVE,
    SHOES
}
